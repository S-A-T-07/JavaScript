//Print the below pattern : Hollow pyramid
//     *
//   *    *
// *        *
//* * * * * * *

for (let row = 1; row <= 4; row++){
    var rowContent = ""
    for (let col = 1; col <= 7; col++){
        if (row + col == 5 || col - row == 3 || row == 4) {
            rowContent+="*"
        }
        else {
            rowContent += " "
        }
    }
    console.log(rowContent);
    
}