for (let row = 1; row <= 4; row++){
    var rowContent = ""
    for (let space = 4; space > row; space--){
        rowContent += " "
    }
    for (let col = 1; col <= row; col++){
        rowContent += "* "
    }
    console.log(rowContent);
    
}